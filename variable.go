package main

import "fmt"

func main() {
	var name string

	name = "Hakim Asrori"
	fmt.Println(name)

	name = "Asep Saepullah"
	fmt.Println(name)

	var mom = "Rini Julianti"
	fmt.Println(mom)

	age := 22
	fmt.Println(age)

	age = 21
	fmt.Println(age)

	var (
		firstName = "Hakim"
		lastName  = "Asrori"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
